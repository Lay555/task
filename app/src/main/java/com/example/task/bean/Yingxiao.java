package com.example.task.bean;

public class Yingxiao {
   private String head;
   private String name;
   private String jiage;
   private String yi;
   private String miaosha;
   private String miao;
   private String time;

    public String getHead() {
        return head;
    }

    public void setHead(String head) {
        this.head = head;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getJiage() {
        return jiage;
    }

    public void setJiage(String jiage) {
        this.jiage = jiage;
    }

    public String getYi() {
        return yi;
    }

    public void setYi(String yi) {
        this.yi = yi;
    }

    public String getMiaosha() {
        return miaosha;
    }

    public void setMiaosha(String miaosha) {
        this.miaosha = miaosha;
    }

    public String getMiao() {
        return miao;
    }

    public void setMiao(String miao) {
        this.miao = miao;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
